  this.color = function () {
    var value = 0, x, y;
    for (x = 0; x < 16; x++) {
      for (y = 0; y < 16; y++) {
        this.hex_trip[value] = String(this.hexbase[x]).toLowerCase() + String(this.hexbase[y]).toLowerCase();
        value++;
      }
    }
  };
