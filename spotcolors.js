  /**
   * Converts the RGB colors to HEX HTML colors
   */
  this.convert_rgb_to_hex = function () {
    this.hex = String(this.hex_trip[this.rgb.red]) + String(this.hex_trip[this.rgb.green]) + String(this.hex_trip[this.rgb.blue]);
  };

  /**
   * Converts the RGB colors to CMYK colors
   */
  this.convert_rgb_to_cmyk = function () {
    var r, g, b, c, m, y, k;

    r = this.rgb.r / 255;
    g = this.rgb.g / 255;
    b = this.rgb.b / 255;
 
    k = Math.min( 1 - r, 1 - g, 1 - b );
    if ((1 - k) == 0 ){
      c = 0 ;
      m = 0 ;
      y = 0 ;
    } else {
    c = ( 1 - r - k ) / ( 1 - k );
    m = ( 1 - g - k ) / ( 1 - k );
    y = ( 1 - b - k ) / ( 1 - k );
    }
    c = Math.round( c * 100 );
    m = Math.round( m * 100 );
    y = Math.round( y * 100 );
    k = Math.round( k * 100 );

    this.cmyk = {
      "c": c,
      "m": m,
      "y": y,
      "k": k
    };
  };

  /**
   * Converts the CMYK colors to RGB colors
   */
  this.convert_cmyk_to_rgb = function () {
    var c = this.cmyk.c / 100;
    var m = this.cmyk.m / 100;
    var y = this.cmyk.y / 100;
    var k = this.cmyk.k / 100;

    var r = 1 - Math.min( 1, c * ( 1 - k ) + k );
    var g = 1 - Math.min( 1, m * ( 1 - k ) + k );
    var b = 1 - Math.min( 1, y * ( 1 - k ) + k );

    r = Math.round(r * 255);
    g = Math.round(g * 255);
    b = Math.round(b * 255);

    this.rgb.r = r;
    this.rgb.g = g;
    this.rgb.b = b;
  };

  /**
   * Converts the HTML HEX colors to RGB colors
   */
  this.convert_hex_to_rgb = function () {
    var red = this.hex.substr(0, 2);
    var green = this.hex.substr(2, 2);
    var blue = this.hex.substr(4, 2);

    var rfound = false;
    for (var i = 0; i < Object.size(this.hex_trip) && !rfound; i++) {
      if (this.hex_trip[i] == red) {
        this.rgb.r = i;
        rfound = true;
      }
    }

    var gfound = false;
    for (var i = 0; i < Object.size(this.hex_trip) && !gfound; i++) {
      if (this.hex_trip[i] == green) {
        this.rgb.g = i;
        gfound = true;
      }
    }

    var bfound = false;
    for (var i = 0; i < Object.size(this.hex_trip) && !bfound; i++) {
      if (this.hex_trip[i] == blue) {
        this.rgb.b = i;
        bfound = true;
      }
    }
  };
